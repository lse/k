/*
 * used to decompose a byte
 */

static int bit_on(char c, int n)
{
  int	mask;

  mask = 1 << (7 - n);
  return c & mask;
}

/*
 * draw some text.
 */

void			draw_text(const char* s,
				  unsigned int x, unsigned int y,
				  t_color fg, t_color bg)
{
  char		c;
  char		ch;
  char		p;
  unsigned int	i;
  unsigned int	j;
  unsigned int	pos;
  unsigned int	strp = 0;

  for (; *s; s++, strp++)
    {
      c = *s;

      for (i = 0; i < 8; ++i)
	for (j = 0; j < 8; ++j)
	  {
	    ch = font[c * 8 + i];
	    p = bit_on(ch, j) ? fg : bg;

	    pos = ((y + i) * GRAPHIC_WIDTH) +
	      (strp * 8 + x) + j;
	    if (!((bg == (unsigned int) -1) && (p == -1)))
	      offbuffer[pos] = p;
	  }
    }
}

/*
 * load an animation.
 * paths is string containing the images name separated by a space.
 * load_anim supports the same image formats as load_image.
 * delay is the displaying time of each image (in ticks).
 *
 * invocation example: load_anim("pic1 pic2 pic3 pic4 pic5", PIC_ANIM_DELAY);
 */

t_anim		*load_anim(char			*paths,
			   int			delay)
{
  char		*p;
  char		*filename;
  t_anim	*anim = NULL;
  int		i;

  if (!paths || !*paths)
    return NULL;

  if (!(anim = malloc(sizeof (t_anim))))
    return NULL;

  anim->nr_img = 1;
  anim->current_img = 0;
  anim->delay = delay;
  anim->jiffies = 0;

  for (p = paths; *p; p++)
    if (*p == ' ')
      anim->nr_img++;

  if (!(anim->imgs = malloc (anim->nr_img * sizeof (t_image))))
    {
      free(anim);
      return NULL;
    }

  p = strdup(paths);

  for (i = 0; i < anim->nr_img; i++)
    {
      filename = p;

      while (*p && *p != ' ')
	p++;

      *p = '\0';
      p++;

      if (!(anim->imgs[i] = load_image(filename)))
	blue_screen("failed to load skater image");
    }

  return anim;
}

/*
 * draw an animation at coordinates (x, y)
 *
 * jiffies is the reference ticks counter which should
 * be incremented at every timer tick.
 */

void		draw_anim(t_anim		*anim,
			  int			x,
			  int			y,
			  unsigned long		jiffies)
{
  if (anim->jiffies + anim->delay <= jiffies || anim->jiffies > jiffies)
    {
      anim->jiffies = jiffies;
      anim->current_img = (anim->current_img + 1) % anim->nr_img;
    }

  draw_image_alpha(anim->imgs[anim->current_img], x, y, 0);
}



